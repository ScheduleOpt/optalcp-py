def test_forbid_start_with_solver():
    """Test forbid_start fluent method with solver."""
    model = cp.Model()

    task = model.interval_var(length=5, name="task")

    # Create calendar: 1=allowed, 0=forbidden
    # Forbid starts during [5, 10)
    calendar = model.step_function([
        (0, 1),   # Initial value: allowed (1)
        (5, 0),   # At point 5: switch to forbidden (0)
        (10, 1)   # At point 10: switch back to allowed (1)
    ])

    # Task cannot start when calendar is 0
    task.forbid_start(calendar)

    # Try to minimize start
    model.minimize(task.start())

    result = cp.solve(model)

    assert result.nb_solutions > 0
    # Should start at 0 (allowed) or 10 (allowed), optimal is 0
    assert result.objective_value == 0, f"Expected start=0, got {result.objective_value}"

    print("✓ test_forbid_start_with_solver passed")


