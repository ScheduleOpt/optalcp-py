def test_fluent_api_with_solver():
    """Test that fluent API works end-to-end with solver."""
    model = cp.Model()

    # Create tasks using fluent API
    x = model.interval_var(length=10, name="x")
    y = model.interval_var(length=10, name="y")
    z = model.interval_var(length=10, name="z")

    # Use fluent methods for precedence
    x.end_before_start(y)
    y.end_before_start(z)

    # Use fluent expression methods
    model.constraint(x.start() >= 0)
    model.constraint(z.end() <= 100)

    # Minimize using fluent API
    model.minimize(z.end())

    # Solve
    result = cp.solve(model)

    assert result.nb_solutions > 0, "Should find solution using fluent API"
    assert result.objective_value == 30, f"Expected objective 30 (0+10+10+10), got {result.objective_value}"

    print(f"✓ test_fluent_api_with_solver passed: objective={result.objective_value}")


def test_fluent_vs_non_fluent_equivalence():
    """Test that fluent API produces same results as non-fluent API."""
    # Model 1: Using fluent API
    model1 = cp.Model()
    x1 = model1.interval_var(length=10, name="x")
    y1 = model1.interval_var(length=10, name="y")
    x1.end_before_start(y1)
    model1.constraint(x1.start() >= 0)
    model1.minimize(y1.end())

    # Model 2: Using non-fluent API (via model methods)
    model2 = cp.Model()
    x2 = model2.interval_var(length=10, name="x")
    y2 = model2.interval_var(length=10, name="y")
    model2.end_before_start(x2, y2)
    model2.constraint(model2.start_of(x2) >= 0)
    model2.minimize(model2.end_of(y2))

    # Both should produce same results
    result1 = cp.solve(model1)
    result2 = cp.solve(model2)

    assert result1.nb_solutions == result2.nb_solutions, "Should find same number of solutions"
    assert result1.objective_value == result2.objective_value, "Should have same objective value"

    print(f"✓ test_fluent_vs_non_fluent_equivalence passed: both got objective={result1.objective_value}")


